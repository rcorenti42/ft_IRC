#include <iostream>
#include "server.hpp"

int main(int argc, char **argv) {
    Server              server;
    int                 port;
    if (argc != 3) {
        std::cerr << "Usage: ./ft_irc [port] [password]" << std::endl;
        return 1;
    }
    server.setPort(argv[1]);
    server.setPassword(argv[2]);
    server.init();
    while (1)
        server.run();
    return 0;
}